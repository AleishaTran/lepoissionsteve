def square(x, y, color=""):
    path.up()
    path.goto(x, y)
    path.down()
    path.fillcolor(color)
    path.begin_fill()
    for _ in range(4):
        path.forward(20)
        path.left(90)
    path.end_fill()

def offset(point):
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    return int(x + y * 20)

def valid(point):
    index = offset(point)
    if tiles[index] == 0:
        return False
    index = offset(point + 19)
    if tiles[index] == 0:
        return False
    return point.x % 20 == 0 or point.y % 20 == 0

def world():
    path.color('white')
    for index in range(len(tiles)):
        tile = tiles[index]
        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)
            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(6, 'white')

def move():
    writer.undo()
    writer.write(state['score'])
    clear()
    world()

    def get_surroundings_state(pacman):
        x = int((pacman.x + 200) / 20)
        y = int((180 - pacman.y) / 20)
        surroundings = []
        for dy in [-1, 0, 1]:
            for dx in [-1, 0, 1]:
                nx, ny = x + dx, y + dy
                if 0 <= nx < 20 and 0 <= ny < 20:
                    index = ny * 20 + nx
                    surroundings.append(tiles[index])
                else:
                    surroundings.append(0)
        return tuple(surroundings)

    # Inside move():
    state_index = get_surroundings_state(pacman)

    # ðŸ§  Use Q-learning or random if unseen
    if state_index not in Q:
        print(f"Unknown state: {state_index}")
        action = np.random.choice(list(actions.keys()))
    else:
        action = int(np.argmax(Q[state_index]))

    direction = actions[action]
    change(direction.x, direction.y)

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)  # Erase background behind pellet
        path.up()
        path.goto(x + 10, y + 10)
        path.dot(6, 'brown')  # Draw brown dot

    pacman_turtle.goto(pacman.x + 10, pacman.y + 10)


    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            plan = choice(list(actions.values()))
            course.x, course.y = plan.x, plan.y
        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'pink')

    update()

    for point, _ in ghosts:
        if abs(pacman - point) < 20:
            print("ðŸ’€ Pacman got caught!")
            return

    ontimer(move, 100)

def change(x, y):
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

